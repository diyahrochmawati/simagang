        <b>SEJARAH SINGKAT PG .KREBET </b><br/><br />
        PT Pabrik Gula Rajawali I merupakan anak perusahaan PT PPEN Rajawali Nusantara Indonesia (Persero) yang 
        bergerak di bidang agro industri berbasis tebu. PT PG Rajawali I didirikan pada tahun 1996 melalui akta 
        notaris nomor 93 tanggal 28 Agustus 1996 yang dibuat dihadapan Notaris Achmad Abid, S.H., CN, pengganti 
        Notaris Sutjipto di Jakarta berdasarkan Surat Penetapan Keterangan Pengadilan Negeri Jakarta Selatan, 
        tertanggal 16 (enam belas) Juli 1996 dengan nomor keputusan 179/Pdt.P/Not/96/PN.Jkt.Se dan Akta Perubahan 
        tersebut telah disetujui dan disahkan oleh Kementerian Hukum dan Hak Asasi Manusia 
        Nomor C2-9513.HT.01.04.TH.96. tanggal 15 Oktober 1996.<br/><br/>
                
        PT PG Rajawali I merupakan penggabungan/Merger Company PT Pabrik Gula Rejo Agung ke dalam PT Pabrik Gula 
        Krebet Baru dengan Baru pada tanggal 28 Agustus 1996 melalui perjanjian penggabungan usaha yang tertuang 
        dalam Akta no. 92 tanggal 28 Agustus 1996 yang dibuat di hadapan Notaris Achmad Abid, S.H., CN, pengganti 
        Notaris Sutjipto di Jakarta berdasarkan Surat Penetapan Keterangan Pengadilan Negeri Jakarta Selatan, 
        tertanggal 16 (enam belas) Juli 1996 dengan nomor keputusan 179/Pdt.P/Not/96/PN.Jkt.Sel.
